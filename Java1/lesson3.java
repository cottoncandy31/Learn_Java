class Main {
  public static void main(String[] args) {
    // int型の変数numberを定義
    int number;
    
    // 変数numberに3を代入
    number = 3;
    
    // 変数numberを出力
    System.out.println(number);

    // String型の変数nameを定義
    String name;
    
    // 変数nameに"Wanko"を代入
    name = "Wanko";
    
    // 変数nameを出力
    System.out.println(name);
    
  }
}