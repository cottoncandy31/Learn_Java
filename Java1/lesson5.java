class Main {
  public static void main(String[] args) {
    int number = 11;
    String text = "Ruby";
    System.out.println(number);
    System.out.println(text);
    
    // 変数numberを9で上書き
    number = 9;
    
    // 変数numberを出力
    System.out.println(number);
    
    // 変数textを「Java」で上書き
    text = "Java";
    
    // 変数textを出力
    System.out.println(text);
    
  }
}